def is_trading_day(ds: str) -> bool:
    """Return True if ds (YYYY-MM-DD) is an NSE equity trading day."""
    d = date.fromisoformat(ds)
    if d.weekday() >= 5:
        return False
    return ds not in _NSE_HOLIDAYS